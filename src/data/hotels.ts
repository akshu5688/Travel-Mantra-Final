export function getHotelsByCity(city: string): Hotel[] {
  const normalized = city.trim().toLowerCase();
  return hotels.filter((h) => h.city.toLowerCase() === normalized);
}

export function getCityCenter(city: string): { lat: number; lng: number } | null {
  const cityHotels = getHotelsByCity(city);
  if (cityHotels.length === 0) return null;
  const lat = cityHotels.reduce((sum, h) => sum + h.lat, 0) / cityHotels.length;
  const lng = cityHotels.reduce((sum, h) => sum + h.lng, 0) / cityHotels.length;
  return { lat, lng };
}
